DELIMITER //
CREATE PROCEDURE get_login(IN empid int)
BEGIN
   SELECT hash, salt from salt where emp_id = empid;
END //
DELIMITER ;

DELIMITER //
CREATE PROCEDURE set_passwords(empider int, hasher varchar(200), salter varchar(200))
BEGIN


IF EXISTS (select * from salt where emp_id = empider) THEN
    update salt set salt = salter where emp_id = empider;
    update salt set hash = hasher where emp_id = empider;
  ELSE
    insert into salt values (empider, hasher, salter);
  END IF;


END //
DELIMITER ;

